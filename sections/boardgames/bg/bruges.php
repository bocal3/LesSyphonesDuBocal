		<h2>But du jeu</h2>
		<p>Le but du jeu est de récolter le moins de cartes Buzz it !</p>
		<p>En effet, celui qui en aura récupéré le plus grand nombre sera déclaré perdant et vivra un grand moment de solitude tandis que ses adversaires se moqueront joyeusement de lui, lui feront faire la vaisselle ou débarrasser la table ...</p>
		<h2>Matériel</h2>
		<ul>
			<li>160 catres comprenant 320 thèmes ;</li>
			<li>1 buzzer.</li>
		</ul>
		<h2>Préparation de la partie</h2>
		<p>Mélanger les cartes et piochez sans les regarder 3 cartes par joueurs avec lesquelles vous formerez une pioche face cachées au milieu de la table.</p>
		<p>Un des joueurs est tiré au sort et sera le premier buzz-master.</p>
		<p>Il prends le buzzer. Il l'allume.</p>
		<h2>Déroulement du jeu</h2>
		<p>Le buzz-master pioche la première carte du talon et prends connaissance des deux thèmes. Il choisit le thème le plus approprié aux joueurs présents.</p>
		<p>Il lit à haute et intelligible vois le thème choisi et lance le jeu en appuyant une première fois sur le bouton réponse du buzzer.</p>
		<p>Le premier joueur qui doit donner une réponse est le joueur situé à sa gauche. S'il réponds correctement dans le temps importi, le buzz-master appuie à nouveau sur le bouton réponse et c'est au joueur suivants.</p>
		<h2>Fin de manche</h2>
		<p>Dès que le gong retentit, le joueur qui devait donner une réponse prend la carte comme preuve de son échec et le buzz-master passe au joueur à la gauche de l'ancien buzz-master.</p>
		<h2>Fin de partie</h2>
		<p>La partie se termine lorsque toutes les cartes ont été jouées.</p>
