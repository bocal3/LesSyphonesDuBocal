		<h2>Matériel et mise en place</h2>
		<p>Tout d’abord, il vous faut mettre le jeu en place, ce qui ne prendra que quelques instants. D’ailleurs, nous allons profiter de cette mise en place pour vous présenter le matériel.</p>
		<ul>
			<li>Vous trouverez 8 meeples abbé, ce qui comprend <strong>1 meeples de chaque couleur</strong>. </li>
		</ul>
		<h2>Déroulement d'une partie</h2>
		<h3>Les abbayes</h3>
		<p>Il est possible de poser un meeple comme <strong>abbé</strong> dans une abbaye. Comme toujours, ce meeple doit provenir de votre réserve.</p>
		<p>Une abbaye est complétée lorsqu’elle est complètement entourée de tuiles. Lors de l’évaluation, une abbaye complétée rapporte <strong>1 point par tuile la complétant</strong> (incluant celle de l’abbaye).</p>
		<p>De plus, vous pourrez décidé de ne déposer <strong>aucun meeple</strong> lors de votre tour afin d'évaluer votre abbé pour le récupérer dans votre réserve.</p>
		<h3>Les jardins</h3>
		<p>Il est possible de poser un meeple comme <strong>abbé</strong> dans un jardin. Comme toujours, ce meeple doit provenir de votre réserve.</p>
		<p>Un jardin est complété lorsqu’il est complètement entourée de tuiles. Lors de l’évaluation, d'un jardin complété rapporte <strong>1 point par tuile la complétant</strong> (incluant celle du jardin).</p>
		<p>De plus, vous pourrez décidé de ne déposer <strong>aucun meeple</strong> lors de votre tour afin d'évaluer votre abbé pour le récupérer dans votre réserve.</p>
