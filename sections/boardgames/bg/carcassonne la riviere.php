		<h2>Matériel et mise en place</h2>
		<p>Tout d’abord, il vous faut mettre le jeu en place, ce qui ne prendra que quelques instants. D’ailleurs, nous allons profiter de cette mise en place pour vous présenter le matériel.</p>
		<ul>
			<li>Vous trouverez 12 tuiles rivières au dos foncé qui remplace la tuile de départ. Lorsque vous joué avec la rivière, remettes la tuile de départ dans la boite. </li>
		</ul>
		<p>Mettes d'abord à l'écart les tuiles <strong>source</strong> et <strong>lac</strong>. Les tuiles restantes sont mélangées face cachée. Placez la <strong>source</strong> comme point de départ. Le <strong>lac</strong> sera la fin de la rivière.</p>
		<h2>Déroulement d'une partie</h2>
		<p>Placez toutes les tuiles rivières en premier lieu avant de jouer avec toutes autres tuiles.</p>
