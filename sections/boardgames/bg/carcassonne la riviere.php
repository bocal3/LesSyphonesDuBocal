		<p>Dans ce jeu, vous aurez pour tâche de développer la région avoisinant Carcassonne. Pour ce faire, vous placerez vos pions sur les routes, dans les villes, les abbayes et les prés. Si vous souhaitez l’emporter, vous devrez faire le meilleur placement possible de vos voleurs, chevaliers, moines et paysans. Autrement, le vainqueur sera l’un de vos adversaires.</p>

